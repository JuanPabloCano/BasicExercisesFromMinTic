    private void Consultar(){
        try{
            int i= 0; Object ob[]= null;
            ConexionBD con= new ConexionBD();
            con.conectarBD();
            String SQL= "SELECT * FROM personas";
            con.resultado= con.sentencia.executeQuery(SQL);
            modelo.setRowCount(0);  
            while (con.resultado.next()) {
                modelo.addRow(ob);
                modelo.setValueAt(con.resultado.getString("td"),i,0);
                modelo.setValueAt(con.resultado.getString("identificacion"),i,1);
                modelo.setValueAt(con.resultado.getString("nombre"),i,2);
                modelo.setValueAt(con.resultado.getString("apellido"),i,3);
                modelo.setValueAt(con.resultado.getString("telefono"),i,4);
                modelo.setValueAt(con.resultado.getString("direccion"),i,5);
                modelo.setValueAt(con.resultado.getString("fecha_ing"),i,6);
                modelo.setValueAt(con.resultado.getString("hora_ing"),i,7);
                modelo.setValueAt(con.resultado.getString("motivo"),i,8);
                i++;
            }
            con.DesconectarBD();
        } catch (SQLException ex){
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
