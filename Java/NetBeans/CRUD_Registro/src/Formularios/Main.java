    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
     
        ConexionBD con= new ConexionBD();
        con.conectarBD();
        String TD= cbTD.getSelectedItem().toString();
        String doc= txtDocumento.getText();
        String nom= txtNombres.getText();
        String ape= txtApellidos.getText();
        String tel= txtTelefono.getText();
        String dir= txtDireccion.getText();
        String fec= txtFecha.getText();
        String hor= txtHora.getText();
        String motiv= txtMotivo.getText();
        if (!doc.isEmpty()){
            if (!nom.isEmpty()){
                if (!ape.isEmpty()){
                    if (!tel.isEmpty()){
                        if (!dir.isEmpty()){
                            if (!fec.isEmpty()){
                                if (!hor.isEmpty()){
                                    if (!motiv.isEmpty()){
                                        try{
                                            String SQL= "INSERT INTO personas"
                                                    + "(td,identificacion,nombre,apellidos,telefono,direccion,fecha_ing,hora_ing,motivo)"
                                                    + "VALUES"
                                                    +"('"+TD+"','"+doc+"','"+nom+"','"+ape+"','"+tel+"','"+dir+"','"+fec+"','"+hor+"','"+motiv+"');";
                                            con.sentencia.execute(SQL);
                                            JOptionPane.showMessageDialog(null, ",Los datos fueron guardados con éxito!");
                                        } catch (SQLException ex){
                                            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE,null,ex);
                                        }
                                } else{
                                        JOptionPane.showMessageDialog(null, "Por favor ingrese el motivo de ingreso");
                                    }
                            } else{
                                    JOptionPane.showMessageDialog(null, "Por favor ingrese la hora de ingreso");
                                }
                        } else{
                                JOptionPane.showMessageDialog(null, "Por favor digite la fecha de ingreso");
                            }
                    } else{
                            JOptionPane.showMessageDialog(null, "Por favor ingrese la dirección");
                        }
                } else{
                        JOptionPane.showMessageDialog(null, "Por favor ingrese el teléfono");
                    }
            } else{
                    JOptionPane.showMessageDialog(null, "Por favor ingrese el apellido");
                }
        } else{
                JOptionPane.showMessageDialog(null, "Por favor ingrese el nombre");
            }
    } else{
            JOptionPane.showMessageDialog(null, "Por favor ingrese el documento");
        }
        con.DesconectarBD();
    }//GEN-LAST:event_btnGuardarActionPerformed

